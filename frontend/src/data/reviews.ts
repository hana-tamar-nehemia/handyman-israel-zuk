function inferCategories(text: string): ReviewCategory[] {
  const categories: ReviewCategory[] = []

  if (/טלוויז|תל(?:ה|ו(?:ת)?)\s*(?:\d+\s*)?טלוויז|מסך\s*(?:טלוויז|LED|LCD)/i.test(text)) {
    categories.push('תליית טלוויזיות')
  }

  if (/ארון|ריהוט|הרכב|הרכי|מיטה|איקאה|שולחן|מגיר|פירוק\s*והרכב/.test(text)) {
    categories.push('הרכבות')
  }

  if (/תריס/.test(text)) {
    categories.push('תריסים')
  }

  if (
    /צבע|גבס|רובה|אריח|מרצפה|סיליקון|טיח|סיד(?:ר)?/.test(text) ||
    (/חורים/.test(text) && /צבע|גבס|סיד|טיח/.test(text))
  ) {
    categories.push('צבע וטיח')
  }

  if (categories.length === 0) {
    categories.push('תיקונים כלליים')
  }

  return categories
}

const reviewById = new Map(
  rawReviews.map((review) => {
    const { categoryTags, ...rest } = review
    return [
      review.id,
      {
        ...rest,
        categories: categoryTags ?? inferCategories(review.text),
      },
    ] as const
  }),
)

export const reviews: Review[] = reviewDisplayOrder
  .map((id) => reviewById.get(id))
  .filter((review): review is Review => review !== undefined)

export const INITIAL_REVIEWS_COUNT = 8
export const MOBILE_INITIAL_REVIEWS_COUNT = 4

export function getReviewsByCategory(category: ReviewCategory): Review[] {
  return reviews.filter((review) => review.categories.includes(category))
}
